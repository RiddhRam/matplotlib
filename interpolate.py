import numpy as np
from scipy.interpolate import interp1d

startingYear = 2017
endingYear = 2037
frames = 500

# Interpolation function
def interpolate_data(x, y, num_points):
    f = interp1d(x, y, kind='cubic')
    x_new = np.linspace(x.min(), x.max(), num_points)
    y_new = f(x_new)
    return x_new, y_new

